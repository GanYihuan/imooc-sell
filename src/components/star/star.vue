<template>
  <div class="star" :class="starType">
    <span
      class="star-item"
      v-for="itemClass in itemClasses"
      :key="itemClass.id"
      :class="itemClass"
    >
      <!-- :key replace track-by="$index" -->
      <!--
        为了给Vue一个提示，以便它可以跟踪每个节点的标识，
        为了重用和重新排序已有的元素，
        您需要为每个项目提供唯一的键属性。
        理想的键值是每个项目的唯一id。
        这个特殊的属性相当于Vue 1。x的轨道。
      -->
    </span>
  </div>
</template>

<script type="text/ecmascript-6">
const LENGTH = 5
const CLS_ON = 'on'
const CLS_HALF = 'half'
const CLS_OFF = 'off'

export default {
	props: {
		size: {
			type: Number
		},
		score: {
			type: Number
		}
	},
	computed: {
		starType() {
			return 'star-' + this.size
		},
		itemClasses() {
			let result = []
			/*
      4.2 -> 4;  4.8 -> 4.5
			Math.floor: 4.9 -> 4
			Math.ceil: 4.1 -> 5
      */
			let score = Math.floor(this.score * 2) / 2
			/* 如果有一个小数，半星 */
			let hasDecimal = score % 1 !== 0
			/* 全星 */
			let integer = Math.floor(score)

			for (let i = 0; i < integer; i++) {
				/* 全星 */
				result.push(CLS_ON)
			}
			if (hasDecimal) {
				/* 半星 */
				result.push(CLS_HALF)
			}
			while (result.length < LENGTH) {
				/* 空星 */
				result.push(CLS_OFF)
			}

			return result
		}
	}
}
</script>

