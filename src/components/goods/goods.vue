<template id="ratings">
  <div>
    <div class="goods">
      <div class="menu-wrapper" ref="menuWrapper">
        <ul>
          <!--
            currentIndex Is generated by the computed attribute,
            determines whether index is equal to currentIndex,
            which is equal to the current class name
          -->
          <li
            class="menu-item"
            v-for="(item,index) in goods"
            :key="index"
            :class="{'current':currentIndex===index}"
            @click="selectMenu(index,$event)"
          >
            <span class="text border-1px">
              <span
                v-show="item.type>0"
                class="icon"
                :class="classMap[item.type]"
              >
              </span>{{item.name}}
            </span>
          </li>
        </ul>
      </div>
      <div class="foods-wrapper" ref="foodsWrapper">
        <ul>
          <li
            class="food-list food-list-hook"
            v-for="(item,index) in goods"
            :key="index"
          >
            <h1 class="title">{{item.name}}</h1>
            <ul>
              <li
                class="food-item border-1px"
                v-for="(food,index) in item.foods"
                :key="index"
                @click="selectFood(food,$event)"
              >
                <div class="icon">
                  <img width="57" height="57" :src="food.icon"/>
                </div>
                <div class="content">
                  <h2 class="name">{{food.name}}</h2>
                  <p class="desc">{{food.description}}</p>
                  <div class="extra">
                    <span class="count">月售{{food.sellCount}}份</span><span>好评率{{food.rating}}%</span>
                  </div>
                  <div class="price">
                    <span class="now">￥{{food.price}}</span>
                    <span class="old" v-show="food.oldPrice">￥{{food.oldPrice}}</span>
                  </div>
                  <div class="cartControl-wrapper">
                    <cartControl @add="addFood" :food="food"></cartControl>
                  </div>
                </div>
              </li>
            </ul>
          </li>
        </ul>
      </div>
      <shopCart
        ref="shopCart"
        :selectFoods="selectFoods"
        :deliveryPrice="seller.deliveryPrice"
        :minPrice="seller.minPrice"
      >
      </shopCart>
    </div>
    <food ref="food" :food="selectedFood" @add="addFood">
    </food>
  </div>
</template>

<script type="text/ecmascript-6">
import BScroll from 'better-scroll'
import shopCart from '../shopCart/shopCart.vue'
import cartControl from '../cartControl/cartControl.vue'
import food from '../food/food.vue'

const ERR_OK = 0

export default {
  components: {
		shopCart,
		food,
		cartControl
	},
  // accpet App.vue pass seller
	props: {
		seller: {
			type: Object
		}
	},
	data() {
		return {
			goods: [],
			/* An array of heights for each dish */
      listHeight: [],
      /* foodsScroll Rolling position */
			scrollY: 0,
			selectedFood: {}
		}
	},
	computed: {
		currentIndex() {
			for (let i = 0; i < this.listHeight.length; i++) {
				// Current height
				let height1 = this.listHeight[i]
				// next height
				let height2 = this.listHeight[i + 1]
				// If there is no next dish, the current dish is the last dish
				// If the current dish is located between the original position and the
				// next dish position from the height of the parent component,
				// return the subscript of the dish.
				if (!height2 || (this.scrollY >= height1 && this.scrollY < height2)) {
					return i
				}
			}
			return 0
		},
		// A collection of all individual dishes.
		selectFoods() {
			let foods = []
			// http://ustbhuangyi.com/sell/api/goods
			// good -> 左侧栏的单个项目
			this.goods.forEach(good => {
				// food -> Individual dishes in the project.
				good.foods.forEach(food => {
					if (food.count) {
						foods.push(food)
					}
				})
			})
			return foods
		}
	},
	created() {
		this.classMap = ['decrease', 'discount', 'special', 'invoice', 'guarantee']
		this.$http.get('/api/goods').then(response => {
      // console.log(response)
			/* get json object */
			response = response.body
			if (response.errno === ERR_OK) {
        this.goods = response.data
        /*
        async
				$nextTick The deferred callback is performed after the next DOM update loop.
				Use this method immediately after modifying the data to get the updated DOM.
        */
				this.$nextTick(() => {
					this._initScroll()
					this._calculateHeight()
				})
			}
		})
	},
	methods: {
		_initScroll() {
			this.meunScroll = new BScroll(this.$refs.menuWrapper, {
				click: true
			})
			this.foodsScroll = new BScroll(this.$refs.foodsWrapper, {
				click: true,
				// In addition to the real-time distribution of scroll events,
				// scroll events can be distributed in real time in the case of swipe.
				// The location of the real-time scrolling.
				probeType: 3
			})
			this.foodsScroll.on('scroll', pos => {
				// scrollY: foodsScroll Rolling position
				this.scrollY = Math.abs(Math.round(pos.y))
			})
		},
		_calculateHeight() {
			// Target element
			let foodList = this.$refs.foodsWrapper.getElementsByClassName(
				'food-list-hook'
			)
			let height = 0
			// listHeight: An array of heights for each dish
			this.listHeight.push(height)
			for (let i = 0; i < foodList.length; i++) {
				let item = foodList[i]
				height += item.clientHeight
				this.listHeight.push(height)
			}
		},
		selectMenu(index, event) {
      // better-scroll
			if (!event._constructed) {
				return
			}
			let foodList = this.$refs.foodsWrapper.getElementsByClassName(
				'food-list-hook'
			)
			let el = foodList[index]
			// target: element, duration: 300ms
			this.foodsScroll.scrollToElement(el, 300)
		},
		selectFood(food, event) {
			// Remove clicks with click events
			if (!event._constructed) {
				return
			}
			this.selectedFood = food
			this.$refs.food.show()
		},
		addFood(target) {
			this._drop(target)
		},
		_drop(target) {
      // 异步执行抛物小球动画，缓解卡顿
			// experience optimization, asynchronous execution of falling animation.
			// $nextTick executes the deferred callback after the next DOM update loop.
			// use this method immediately after modifying the data to get the updated DOM.
			this.$nextTick(() => {
				this.$refs.shopCart.drop(target)
			})
		}
	}
}
</script>

