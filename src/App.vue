<template>
  <div id="app">
    <v-header :seller="seller"></v-header>
    <div class="tab border-1px">
      <div class="tab-item">
        <router-link to="/goods">商品</router-link>
      </div>
      <div class="tab-item">
        <router-link to="/ratings">评论</router-link>
      </div>
      <div class="tab-item">
        <router-link to="/seller">商家</router-link>
      </div>
    </div>
    <!--
      keep-alive: 包裹动态组件时，会缓存不活动的组件实例，而不是销毁它们
      当组件在 <keep-alive> 内被切换，它的 activated 和 deactivated
      这两个生命周期钩子函数将会被对应执行
    -->
    <!-- <keep-alive exclude="Detail"> Detail 组件不缓存处理 -->
    <keep-alive>
      <router-view :seller="seller"></router-view>
    </keep-alive>
  </div>
</template>

<script type="text/ecmascript-6">
import { urlParse } from 'common/js/util'
import header from 'components/header/header.vue'

const ERR_OK = 0

export default {
	components: {
		/* v-header: prevent conflict */
		'v-header': header
	},
	/* handle data, data need watch */
	data() {
		return {
			seller: {
				/* 直接运行函数 */
				id: (() => {
					/* 从 url 获取 id */
					let queryParam = urlParse()
					return queryParam.id
				})()
			}
		}
	},
	/* handle data, data not need watch */
	created() {
		/* vue-resource ajax request */
		this.$http.get('/api/seller?id=' + this.seller.id).then(res => {
			/* get res.body(json object) */
			res = res.body
			if (res.errno === ERR_OK) {
				/* [res.data](http://localhost:8088/api/seller) */
				/* Object.assign: extend attributes to object */
        this.seller = Object.assign({}, this.seller, res.data)
        console.log(this.seller)
			}
		})
	}
}
</script>

